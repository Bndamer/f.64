const express = require("express");
const router = express.Router();

const controller = require("../controller/inscripciones.controlador");

/////////////////////////////////////////////////////////
////////////////////// GET //////////////////////////////
/////////////////////////////////////////////////////////

// Traer todas las inscripciones
router.get('/', controller.allInscripciones);

// Traer desafíos de un usuario específico
router.get('/usuario/:idUsuario', controller.misDesafios);

// Traer una inscripción específica
router.get('/:id', controller.showInscripcion);


/////////////////////////////////////////////////////////
////////////////////// POST /////////////////////////////
/////////////////////////////////////////////////////////

// Crear nueva inscripción (inscribirse a desafío)
router.post('/', controller.newInscripcion);

/////////////////////////////////////////////////////////
////////////////////// PUT //////////////////////////////
/////////////////////////////////////////////////////////

// Actualizar inscripción (progreso, estado, puntos)
router.put('/:id', controller.updateInscripcion);

/////////////////////////////////////////////////////////
////////////////////// DELETE ///////////////////////////
/////////////////////////////////////////////////////////

// Eliminar inscripción
router.delete('/:id', controller.destroyInscripcion);

module.exports = router;